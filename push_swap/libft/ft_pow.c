int		ft_pow(int n, int power)
{
	int	nbr;
	int	i;

	i = 0;
	nbr = 1;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (++i <= power)
		nbr *= n;
	return (nbr);
}
